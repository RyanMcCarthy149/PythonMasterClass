#This is a string inside the brackets. Single or double quotes are needed
print("Hello World")

#Print on seperate lines
print("Hello World 1")
print("Hello World 2")
print("Hello World 3")

#Print multiple on the same print statement but different line
print("Hello World 1\nHello World 2\nHello World 3")

#Print 2 words together (Concat strings)
print("Hello" + "world")
print("Hello" + " " + "world")

#Using variables to print - No quotes needed in print statement
x = "Ryan"
y = "McCarthy"
print(x) #Print just Ryan
print(x + y) #Print Full name together no spaces
print(f"{x} {y}") #Use f to print name with spaces

